export function filterNationalities(
  countryArray: string[],
  searchTerm: string
) {
  // Convert search term to lowercase for case-insensitive matching
  searchTerm = searchTerm.toLowerCase();

  // Use Array.filter() to filter countries based on the search term
  return countryArray.filter(function (country: string) {
    // Convert country name to lowercase for case-insensitive matching
    return country.toLowerCase().includes(searchTerm);
  });
}

